package day06;

public class Quiz {
	String exercise;
	int answer;
	int feedCount;
	boolean jackpot;
	
	
	
	public Quiz(String exercise, int answer, int feedCount) {
		this.exercise = exercise;
		this.answer = answer;
		this.feedCount = feedCount;
	}


	public Quiz(String exercise, int answer, int feedCount, boolean jackpot) {
		this.exercise = exercise;
		this.answer = answer;
		this.feedCount = feedCount;
		this.jackpot = jackpot;
	}
	
	
	
	
	
	
	
	
	
	
	
}
